// Numbers
console.log(10)
console.log(3.14)

// Strings
console.log('Hello world!') // single quotes
console.log("Hi Everyone!") // double quotes
console.log(`What's going on!`) // back ticks

// Boolean
console.log(true)
console.log(false)

// Undefined 
console.log(undefined)

// Null
console.log(null)

// typeof operator
console.log(typeof 10) // number
console.log(typeof 'Hello World!') // string
console.log(typeof true) // boolean
console.log(typeof false) // boolean
console.log(typeof undefined) // undefined
console.log(typeof null) // object

// Math Operators
console.log(10 + 5) // -> 15
console.log(10 - 5) // -> 5
console.log(5 * 4) // -> 20
console.log(15 / 3) // -> 5
console.log(3 ** 2) // -> 9
console.log(16 / 3) // -> 16.333333
console.log(16 % 3) // -> 1
console.log(750 % 10) // -> 0
console.log(30 % 2) // -> 0

// Logical Operators (returns boolean value)
console.log(5 == 10) // -> false
console.log(5 !== 10) // -> true
console.log(5 >= 5) // -> true
console.log(5 < 10) // -> true
console.log(4 <= 5) // -> true
console.log(5 == '5') // -> true (check value)
console.log(5 === '5') // -> false (checks value AND type)
console.log(typeof 5) // -> number
console.log(typeof '5') // -> string

// -- OR
console.log(5 > 10 || 5 > 2) // -> true
console.log(5 > 10 || 5 < 2) // -> false

// -- AND
console.log(5 > 10 && 5 > 2) // -> false
console.log(2 > 1 && 10 > 5) // -> true
console.log(5 < 10 && 6 > 2 && 6 % 2 == 0) // -> true


